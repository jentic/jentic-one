// unmarshalConfig renders the draft and parses it back into a generic map so
// tests can assert on structure without pinning exact byte output.
func renderToMap(t *testing.T, d *Draft) map[string]any {
	t.Helper()
	data, err := d.Render()
	if err != nil {
		t.Fatalf("Render: %v", err)
	}
	if !strings.HasPrefix(string(data), "# Generated by 'jenticctl install'.") {
		t.Errorf("missing generated header")
	}
	var out map[string]any
	if err := yaml.Unmarshal(data, &out); err != nil {
		t.Fatalf("unmarshal rendered config: %v", err)
	}
	return out
}

func TestRenderSQLite(t *testing.T) {
	d := NewDraft()
	d.DBBackend = BackendSQLite
	d.SQLiteDir = "/data"
	out := renderToMap(t, d)

	dbs := out["databases"].(map[string]any)
	reg := dbs["registry"].(map[string]any)
	if reg["backend"] != "sqlite" {
		t.Errorf("registry backend = %v", reg["backend"])
	}
	if reg["path"] != "/data/registry.db" {
		t.Errorf("registry path = %v", reg["path"])
	}
	// Lexical search block is emitted for every backend.
	if _, ok := out["search"]; !ok {
		t.Errorf("expected search block for sqlite")
	}
	// No SSO by default: auth has no idp.
	auth := out["auth"].(map[string]any)
	if _, ok := auth["idp"]; ok {
		t.Errorf("did not expect idp block without SSO")
	}
}

