func TestRenderSSO(t *testing.T) {
	d := NewDraft()
	d.SSOEnabled = true
	d.SSOClientID = "client-123"
	d.SSOClientSecret = "secret-abc"
	if err := d.FillSecrets(); err != nil {
		t.Fatalf("FillSecrets: %v", err)
	}
	out := renderToMap(t, d)

	auth := out["auth"].(map[string]any)
	idp, ok := auth["idp"].(map[string]any)
	if !ok {
		t.Fatalf("expected idp block when SSO enabled")
	}
	if idp["provider"] != "google" || idp["client_id"] != "client-123" {
		t.Errorf("unexpected idp: %+v", idp)
	}
	if idp["issuer"] != "https://accounts.google.com" {
		t.Errorf("idp issuer = %v", idp["issuer"])
	}
	signing, ok := auth["id_signing"].([]any)
	if !ok || len(signing) != 1 {
		t.Fatalf("expected one id_signing entry, got %v", auth["id_signing"])
	}
	entry := signing[0].(map[string]any)
	if !strings.Contains(entry["private_key_pem"].(string), "BEGIN PRIVATE KEY") {
		t.Errorf("id_signing key not a PEM: %v", entry["private_key_pem"])
	}
}

